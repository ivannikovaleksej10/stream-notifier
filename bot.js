const TelegramBot = require('node-telegram-bot-api');
const db = require('./db/database');
const config = require('./config/config');

const ADMIN_CHAT_ID = 210897312; // –£–∫–∞–∂–∏—Ç–µ –≤–∞—à Telegram ID

const TOKEN = config.telegram.botToken;
const bot = new TelegramBot(TOKEN, { polling: true });

// –î–æ–±–∞–≤–ª–µ–Ω–∏–µ –Ω–æ–≤–æ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –≤ –±–∞–∑—É
bot.on('message', (msg) => {
  const chatId = msg.chat.id;

  // –î–æ–±–∞–≤–ª—è–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –≤ –±–∞–∑—É, –µ—Å–ª–∏ –µ–≥–æ —Ç–∞–º –µ—â—ë –Ω–µ—Ç
  db.run('INSERT OR IGNORE INTO users (chat_id) VALUES (?)', [chatId], function (err) {
    if (err) {
      console.error("–û—à–∏–±–∫–∞ –ø—Ä–∏ –¥–æ–±–∞–≤–ª–µ–Ω–∏–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –≤ –±–∞–∑—É:", err.message);
    }
  });

  bot.sendMessage(chatId, '–í—ã –ø–æ–¥–ø–∏—Å–∞–Ω—ã –Ω–∞ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏—è –æ —Å—Ç—Ä–∏–º–µ.');
});

// –û–±—Ä–∞–±–æ—Ç–∫–∞ –∫–æ–º–∞–Ω–¥—ã /stats (—Ç–æ–ª—å–∫–æ –¥–ª—è –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞)
bot.onText(/\/stats/, (msg) => {
  const chatId = msg.chat.id;

  if (chatId !== ADMIN_CHAT_ID) {
    return bot.sendMessage(chatId, "–£ –≤–∞—Å –Ω–µ—Ç –ø—Ä–∞–≤ –Ω–∞ –ø—Ä–æ—Å–º–æ—Ç—Ä —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏.");
  }

  // –ü–æ–ª—É—á–∞–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–æ–¥–ø–∏—Å–∞–Ω–Ω—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π
  db.get('SELECT COUNT(*) AS count FROM users', (err, row) => {
    if (err) {
      return bot.sendMessage(chatId, "–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏ —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏.");
    }

    bot.sendMessage(
      chatId,
      `üìä *–°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞ –±–æ—Ç–∞:*\n\nüë• –ü–æ–¥–ø–∏—Å—á–∏–∫–æ–≤: *${row.count}*`,
      { parse_mode: "Markdown" }
    );
  });
});

